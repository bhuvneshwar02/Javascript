function categorizeNumbers(num){

    switch(num){
        case 0:
            console.log('Zero');
            break;
        case 1:
            console.log('one');
            break;
        case 2:
            console.log('two');
            break;
        case 3:
            console.log('three');
            break;
        default:
            console.log('Invalid number');
        }
}
categorizeNumbers(0);
categorizeNumbers(1);
categorizeNumbers(2);
categorizeNumbers(3);
categorizeNumbers(4);